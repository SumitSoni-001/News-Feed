    public static String DateFormat(String oldstringDate) {
        String newDate = null;
        SimpleDateFormat dateFormat = new SimpleDateFormat("hh:mm a',' dd MMM yyyy", new Locale(getCountry()));
        try {
            Date date = new SimpleDateFormat("yyyy-MM-dd'T'HH:mm:ss'Z'").parse(oldstringDate); /** The time provided in SimpleDateFormat() above, is the time format provided by the API, and the
             * time format stored in 'dateFormat' variable is the format we specified acc. to our need. */

            newDate = dateFormat.format(date);
        } catch (ParseException e) {
            e.printStackTrace();
            newDate = oldstringDate;
        }

        return newDate;
    }

    public static String getCountry() {
        Locale locale = Locale.getDefault();
        String country = String.valueOf(locale.getCountry());
        return country.toLowerCase();
    }
